#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int	a;

	a = 0;
	if (s1 != NULL && s2 != NULL)
	{
		while ((*s1 || *s2) && n > 0)
		{
			if (*s1 != *s2)
			{
				a = 1;
				break ;
			}
			s2++;
			s1++;
			n--;
		}
		if (a == 0)
			return (1);
	}
	return (0);
}
