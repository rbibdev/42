int		ft_strncmp(char *s1, const char *s2, unsigned int n)
{
	while ((*s1 || *s2) && n > 0)
	{
		if (*s1 != *s2)
		{
			return ((unsigned char)*s1 - (unsigned char)*s2);
		}
		s2++;
		n--;
		s1++;
	}
	return (0);
}
