int		ft_atoi(const char *str)
{
	int	sign;
	int	x;

	sign = 0;
	x = 0;
	while (*str == 32 || (*str >= 8 && *str <= 13))
		str++;
	if (*str == 45)
	{
		sign = -1;
	}
	if (*str == 43 || *str == 45)
		str++;
	while (*str > 47 && *str < 58)
	{
		x = x * 10 + (*str - 48);
		str++;
	}
	if (sign == -1)
		return (-x);
	return (x);
}
